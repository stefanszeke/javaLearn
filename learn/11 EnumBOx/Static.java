// static import Math:
import static java.lang.Math.*;

public class Static {
    public static void main(String[] args) {
        System.out.println("sqrt(900.0) = " + sqrt(900.0));
        System.out.println("ceil(-9.8) = " + ceil(-9.8));
        System.out.println("E = " + E);
        System.out.println("PI = " + PI);
    }
}
